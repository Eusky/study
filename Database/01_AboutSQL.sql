/*
SQL 종류
1. DDL(Data Definition Language)
  1)데이터 정의어
  2) 데이터베이스, 테이블, 사용자 등의 정보를 다루는 언어
  3) 종류
    (1) CREATE: 생성
    (2) ALTER: 수정
    (3) DROP: 삭제
    (4) TRUNCATE: 테이블의 모든 데이터를 삭제(테이블 구조는 유지, 롤백 불가)
        
2. DML(Data Manipulation Language)
  1) 데이터 조작어
  2) 실제 데이터를 다루는 언어
  3) 종류
    (1) INSERT: 삽입
    (2) UPDATE: 갱신
    (3) DELETE: 삭제 
    
3. DQL(Data Query Language)
  1) 데이터 질의어
  2) 데이터를 검색할 때 사용하는 언어
  3) 종류
    (1) SELECT: 선택

4. DCL(Data Control Language)
  1) 데이터 제어어
  2) 사용자의 권한을 제어할 때 사용하는 언어
  3) 종류
    (1) GRANT: 승인
    (2) REVOKE: 승인 해제
    
5. TCL(Transaction Control Language)
  1) 트랜잭션 제어어
  2) 트랜잭션을 처리할 때 사용하는 언어
  3) 종류
    (1) COMMIT: 트랜잭션 성공(작업 내용 저장)
    (2) ROLLBACK: 트랜잭션 실패(작업 내용 취소)
*/